// =============================================================================
// AiState.cs
// =============================================================================
// 概要:
//   敵AIの状態を表す列挙型。トルネコ/シレン風の状態マシンを実現します。
//   各敵のAiMemory.CurrentStateに格納され、行動選択の基準となります。
//
// 状態遷移図:
//   ┌─────────┐
//   │ Sleeping │─────[プレイヤー接近/被ダメージ]──→┐
//   └─────────┘                                    │
//         ↑                                        ↓
//   ┌─────────┐    [見失う+忘却]    ┌──────────┐
//   │Wandering │←──────────────────│ Returning │
//   └─────────┘                     └──────────┘
//         │                              ↑
//   [プレイヤー発見]                [見失う]
//         ↓                              │
//   ┌─────────┐                          │
//   │ Pursuing │←────────────────────────┤
//   └─────────┘                          │
//         │                              │
//   [射程内に入る]                       │
//         ↓                              │
//   ┌──────────┐    [射程外]    ─────────┘
//   │ Attacking │
//   └──────────┘
//         │
//   [HP低下]
//         ↓
//   ┌─────────┐
//   │ Fleeing  │
//   └─────────┘
//
// 特殊状態:
//   - Confused: 状態異常による混乱（ランダム移動）
//   - Paralyzed: 状態異常による麻痺（行動不能）
//
// 使用場所:
//   - AiMemory.CurrentState: 現在の状態を保持
//   - AdvancedEnemyAiService.UpdateMemory(): 状態遷移を管理
//   - 各IEnemyBehavior.CanExecute(): 行動可否の判定
// =============================================================================

namespace Roguelike.Domain.Gameplay.Enemies.Enums
{
    /// <summary>
    /// 敵AIの状態を表します。
    /// </summary>
    /// <remarks>
    /// トルネコ/シレン風の状態マシンを実現します。
    /// 各敵のAiMemory.CurrentStateに格納され、行動選択の基準となります。
    /// </remarks>
    public enum AiState
    {
        /// <summary>
        /// 眠っている状態。
        /// プレイヤーが接近するか攻撃を受けると起きます。
        /// </summary>
        Sleeping,

        /// <summary>
        /// 徘徊状態。
        /// プレイヤーを認識していないときにランダムに動きます。
        /// </summary>
        Wandering,

        /// <summary>
        /// 追跡状態。
        /// プレイヤーを視認しており、追いかけています。
        /// </summary>
        Pursuing,

        /// <summary>
        /// 攻撃状態。
        /// 攻撃射程内にプレイヤーがいます。
        /// </summary>
        Attacking,

        /// <summary>
        /// 逃走状態。
        /// HPが閾値以下になり、プレイヤーから逃げています。
        /// </summary>
        Fleeing,

        /// <summary>
        /// 帰還状態。
        /// プレイヤーを見失い、最後に見た場所へ向かっています。
        /// </summary>
        Returning,

        /// <summary>
        /// 混乱状態。
        /// ランダムな方向に動きます。
        /// </summary>
        Confused,

        /// <summary>
        /// 麻痺状態。
        /// 行動できません。
        /// </summary>
        Paralyzed
    }
}


