// =============================================================================
// IntelligenceLevel.cs
// =============================================================================
// 概要:
//   敵の知性レベルを表す列挙型。行動の賢さに影響します。
//
// 知性レベルと行動の違い:
//   Low (低知性):
//     - パス探索を使わず貪欲法で移動
//     - 罠にかかる
//     - 予測可能なパターンで動く
//     - 例: スライム、ゾンビ
//
//   Medium (中知性):
//     - パス探索を使って障害物を迂回
//     - 罠を回避する
//     - 基本的な戦術を使う
//     - 例: ゴブリン、オーク
//
//   High (高知性):
//     - パス探索を使い、最適な行動を選択
//     - 複雑な戦術、アイテム使用、協調行動
//     - 逃走や待ち伏せなどの高度な判断
//     - 例: ウィザード、ボス敵
//
// 実装での使用:
//   - EnemyProfile.Intelligence: 敵種別ごとの知性設定
//   - PursuitBehavior.Execute(): 知性に応じてパス探索を使用するか判定
//   - 将来的な拡張: 罠回避、アイテム使用判断等
// =============================================================================

namespace Roguelike.Domain.Gameplay.Enemies.Enums
{
    /// <summary>
    /// 敵の知性レベルを表します。
    /// </summary>
    /// <remarks>
    /// 行動の賢さに影響します。
    /// 高知性の敵はパス探索を使い、低知性の敵は貪欲法で移動します。
    /// </remarks>
    public enum IntelligenceLevel
    {
        /// <summary>
        /// 低知性。
        /// 罠にかかる、予測可能なパターンで動く。
        /// パス探索を使わず貪欲法で移動します。
        /// </summary>
        Low,

        /// <summary>
        /// 中知性。
        /// 罠を回避する、基本的な戦術を使う。
        /// パス探索を使って障害物を迂回します。
        /// </summary>
        Medium,

        /// <summary>
        /// 高知性。
        /// 複雑な戦術、アイテム使用、協調行動。
        /// パス探索を使い、最適な行動を選択します。
        /// </summary>
        High
    }
}


