// =============================================================================
// SpeedType.cs
// =============================================================================
// 概要:
//   敵の行動速度タイプを表す列挙型。1ターンあたりの行動回数に影響します。
//
// 速度と行動回数:
//   - Half: 2ターンに1回行動（鈍足）
//   - Normal: 1ターンに1回行動（標準）
//   - Double: 1ターンに2回行動（倍速）
//   - Triple: 1ターンに3回行動（3倍速、ボス等）
//
// 実装での使用:
//   - EnemyProfile.Speed: 敵種別ごとの速度設定
//   - AiMemory.ActionsThisTurn: このターンの行動回数カウント
//   - AdvancedEnemyAiService: 速度に応じた行動回数制御
//
// ゲームデザイン上の意図:
//   - 鈍足敵: 強力だが回避しやすい（ドラゴン等）
//   - 倍速敵: 素早い動きで脅威を与える（盗賊等）
//   - 3倍速: ボス戦での緊張感を演出
// =============================================================================

namespace Roguelike.Domain.Gameplay.Enemies.Enums
{
    /// <summary>
    /// 敵の行動速度タイプを表します。
    /// </summary>
    /// <remarks>
    /// 1ターンあたりの行動回数に影響します。
    /// AdvancedEnemyAiServiceで速度に応じた行動回数制御を行います。
    /// </remarks>
    public enum SpeedType
    {
        /// <summary>
        /// 鈍足。2ターンに1回行動します。
        /// </summary>
        Half,

        /// <summary>
        /// 通常速度。1ターンに1回行動します。
        /// </summary>
        Normal,

        /// <summary>
        /// 倍速。1ターンに2回行動します。
        /// </summary>
        Double,

        /// <summary>
        /// 3倍速。1ターンに3回行動します（ボス等）。
        /// </summary>
        Triple
    }
}


